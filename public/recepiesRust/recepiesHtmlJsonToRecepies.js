function parseItems(itemsStr) {
    const regex = /(\d+\.?\d*)×?\s*([A-Za-z\s-]+)(\d+\.?\d*)\s*\/min/g;
    const items = [];
    let match;

    while ((match = regex.exec(itemsStr)) !== null) {
        const [, quantity, name, rate] = match;
        items.push([name.trim(), parseFloat(rate)]);
    }
    return items;
}

